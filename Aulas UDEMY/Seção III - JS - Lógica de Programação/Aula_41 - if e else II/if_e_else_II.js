/*

Exemplos if e else:

*/

const num = 11;

if (num >= 0 && num <= 1){
    console.log('O número está entre 0 e 1');
} else if (num >= 2 && num <= 3){
    console.log('O número está entre 2 e 3');
} else if (num >= 4 && num <= 5){
    console.log('O número está entre 4 e 5');
} else if (num >= 6 && num <= 7){
    console.log('O número está entre 6 e 7');
} else if (num >= 8 && num <= 10) {
    console.log('O número está entre 8 e 10');
} else {
    console.log('O número não foi encontrado!')
}